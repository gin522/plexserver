namespace SocketHttpListener
{
  /// <summary>
  /// Contains the values that indicate whether the byte order is a Little-endian or Big-endian.
  /// </summary>
  public enum ByteOrder : byte
  {
    /// <summary>
    /// Indicates a Little-endian.
    /// </summary>
    Little,
    /// <summary>
    /// Indicates a Big-endian.
    /// </summary>
    Big
  }
}
